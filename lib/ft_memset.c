#include "libft.h"

void	*ft_memset(void *mem, int c, size_t num)
{
	unsigned char	*p;
	size_t			i;

	i = 0;
	p = mem;
	while (i < num)
	{
		*p = c;
		p++;
		i++;
	}
	return (mem);
}
